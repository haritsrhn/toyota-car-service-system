    public static void writeFile()throws IOException{
        
        FileWriter write = new FileWriter("ServiceInfo.txt");
        PrintWriter teks = new PrintWriter(write);
        
        teks.printf("%-15s%-15s%-15s%-10s%-15s%-15s%-15s%-15s", "Cust. Name", "Plate No", "Car Model", "CC",
                "Kilometer", "Membership", "Services", "Payment");
        
        for(CarServiceBill a: carList)
            teks.write(a.toStringForFile());
        write.close();
        
    }
    

