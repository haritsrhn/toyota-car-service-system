    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        
        try{
        String custName = nameTF.getText();
        String plateNo = plateTF.getText();
        String carModel = modelTF.getText();
        double cc = Double.parseDouble(ccTF.getText());
        String kmStr = (String)kmCB.getSelectedItem();
        int km = Integer.parseInt(kmStr);
        String member = (String)memberCB.getSelectedItem();
        
        double total = 0;
        String services = "";
        if(moCheck.isSelected()){
            
            total += 34.00;
            services +="-Motor Oil-";
            
        }
        if(ofCheck.isSelected()){
            
            total +=30.00;
            services += "-Oil Filter-";
            
        }
        if(gCheck.isSelected()){
            
            total += 5.00;
            services += "-Gasket-";
            
        }
        if(wwCheck.isSelected()){
            
            total += 5.00;
            services += "-Window Wahser-";
            
        }
        if(ariCheck.isSelected()){
            
            total+=50.00;
            services+="-Anti Rust-";
            
        }
        if(waCheck.isSelected()){
            
            total+=80.00;
            services+="-Wheel Alignment-";
            
        }
        
        if(member.equals("Yes")){
            
            CarServiceBill bill = new CarServiceBill(custName, plateNo, carModel, cc, km, true, services);
            carList.add(bill);
            bill.determineFinalPayment(total);
            outputTA.setText(bill.toString());
            JOptionPane.showMessageDialog(null, "Data is saved.", "Information", JOptionPane.INFORMATION_MESSAGE);
            
        }
        else{
            
           CarServiceBill bill = new CarServiceBill(custName, plateNo, carModel, cc, km, false, services);
           carList.add(bill);
            bill.determineFinalPayment(total);
            outputTA.setText(bill.toString());
            JOptionPane.showMessageDialog(null, "Data is saved.", "Information", JOptionPane.INFORMATION_MESSAGE); 
            
        }
        writeFile();
        
        }catch(NumberFormatException e){
            
            JOptionPane.showMessageDialog(null, "Invalid Input!", "Error", JOptionPane.ERROR_MESSAGE);
            
        }catch(Exception e){
            
            System.out.print(e.getMessage());
        }
    }//GEN-LAST:event_saveBtnActionPerformed

